public class getting_started_and_working_with_variables {
    public static void main(String[] args) {

        System.out.println("Hello World");
        int i = 5;
        i = 6;
        System.out.println(i);
        double d = 5.5; // by default in Java all floating numbers are in 'double'
        System.out.println(d);
        float f = 5.6f; // if you want to use 'float' you have to write 'f' after the float number
        System.out.println(f);
    }
}
